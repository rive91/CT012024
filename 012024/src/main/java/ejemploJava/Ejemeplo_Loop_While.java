package ejemploJava;

public class Ejemeplo_Loop_While {

	public static void main(String[] args) {

		int a = 1;
		
		while (a <= 10) { //Va a salir del ciclo cuando la variable sea mayor de 10
			System.out.println("el valor de A: " + a);
			a++; //incremeanta el valor de la variable para la siguiente iteracion
		}
	}

}
